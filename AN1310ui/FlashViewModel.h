#ifndef FLASHVIEWMODEL_H
#define FLASHVIEWMODEL_H

#include <QAbstractTableModel>

#include "Bootload/DeviceData.h"
#include "Bootload/Device.h"

/*!
 * Provides GUI formatting of FLASH program memory for display in a QTableView.
 */
class FlashViewModel : public QAbstractTableModel
{
    Q_OBJECT

public:
    FlashViewModel(Device* device, DeviceData* deviceData, QObject *parent = 0);

    int rowCount(const QModelIndex &parent = QModelIndex()) const;
    int columnCount(const QModelIndex &parent = QModelIndex()) const;

    QVariant data(const QModelIndex &index, int role = Qt::DisplayRole) const;
    QVariant headerData(int section, Qt::Orientation orientation, int role = Qt::DisplayRole) const;

    void setVerifyData(DeviceData* data);
    bool hasVerifyData(void);

protected:
    DeviceData* deviceData;
    DeviceData* verifyData;
    Device* device;
};

#endif // FLASHVIEWMODEL_H
